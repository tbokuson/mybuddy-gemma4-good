    /// 入力テキストに指定のキーワードのいずれかが含まれるかを判定
    static func containsAny(_ text: String, keywords: [String]) -> Bool {
        let lowered = text.lowercased()
        return keywords.contains { lowered.contains($0.lowercased()) }
    }
